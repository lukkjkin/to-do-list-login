function MenuDrop_va(){
    var arrow_right_va = document.getElementById("arrow_right_va")
    var arrow_drop_va = document.getElementById("arrow_drop_va")
    var ul_va = document.getElementById("ul_va")

    if (arrow_right_va.style.display == "block"){
        arrow_right_va.style.display = "none"
        arrow_drop_va.style.display = "block"
        ul_va.style.display = "block"
    }else{
        arrow_right_va.style.display = "block"
        arrow_drop_va.style.display = "none"
        ul_va.style.display = "none"
    }
}
